enum MapMode {
  normal('Normal', Icons.map, nightMode),
  dark('Dark', Icons.nightlight_round, darkMode),
  retro('Retro', Icons.color_lens, retroMode),
  silver('Silver', Icons.brightness_auto, silverMode);

  final String name;
  final IconData icon;
  final String style;

  const MapMode(this.name, this.icon, this.style);
}

String getMapStyle(MapMode mode) {
  return mode.style;
}

IconData getMapModeIcon(MapMode mode) {
  return mode.icon;
}

String getMapModeName(MapMode mode) {
  return mode.name;
