double? parseDouble(dynamic value) {
  if (value == null) return null;
  if (value is double) return value;
  if (value is int) return value.toDouble();
  if (value is String) {
    try {
      return double.parse(value);
    } catch (_) {
      return null;
    }
  }
  return null;
}

Color getColorForProviderType(String providerType) {
  switch (providerType) {
    case 'cleaner':
      return Colors.green;
    case 'plumber':
      return Colors.blue;
    case 'electrician':
      return Colors.orange;
    default:
      return Colors.purple;
  }
}

Widget getProviderIcon(String providerType, Color color, double size) {
  switch (providerType) {
    case 'cleaner':
      return Icon(Icons.cleaning_services, color: color, size: size);
    case 'plumber':
      return Icon(Icons.plumbing, color: color, size: size);
    case 'electrician':
      return Icon(Icons.electrical_services, color: color, size: size);
    default:
      return Icon(Icons.person, color: color, size: size);
  }
}

LatLngBounds createBounds(List<LatLng> points) {
  double? west, south, east, north;

  for (LatLng point in points) {
    west = west != null
        ? (point.longitude < west ? point.longitude : west)
        : point.longitude;
    south = south != null
        ? (point.latitude < south ? point.latitude : south)
        : point.latitude;
    east = east != null
        ? (point.longitude > east ? point.longitude : east)
        : point.longitude;
    north = north != null
        ? (point.latitude > north ? point.latitude : north)
        : point.latitude;
  }

  return LatLngBounds(
    southwest: LatLng(south!, west!),
    northeast: LatLng(north!, east!),
  );
}


